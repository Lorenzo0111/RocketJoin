package me.lorenzo0111.rocketjoin.bungeecord;

import me.lorenzo0111.rocketjoin.bungeecord.utilities.PluginLoader;
import net.md_5.bungee.api.plugin.Plugin;
import net.md_5.bungee.config.Configuration;
import net.md_5.bungee.config.ConfigurationProvider;
import net.md_5.bungee.config.YamlConfiguration;

import java.io.File;
import java.io.IOException;
import java.io.InputStream;
import java.nio.file.Files;

public class RocketJoinBungee extends Plugin {

    private Configuration configuration;

    @Override
    public void onEnable() {
        this.loadConfig();

        if (!getDataFolder().exists())
            getDataFolder().mkdir();

        File file = new File(getDataFolder(), "config.yml");


        if (!file.exists()) {
            try (InputStream in = getResourceAsStream("config.yml")) {
                Files.copy(in, file.toPath());
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        PluginLoader loader = new PluginLoader(this);
        loader.loadUpdater();
        loader.loadMetrics();
        loader.registerEvents();
        loader.placeholderHook();
    }

    @Override
    public void onDisable() {
    }

    public void loadConfig() {
        try {
            this.configuration = ConfigurationProvider.getProvider(YamlConfiguration.class).load(new File(getDataFolder(), "config.yml"));
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public void reloadConfig() {
        this.loadConfig();
    }

    public Configuration getConfig() {
        return configuration;
    }
}
